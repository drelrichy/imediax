"use client";

import Link from "next/link";
import { useState } from "react";
import { useRouter } from "next/navigation";

export default function RegisterForm() {
  const [username, setUsername] = useState("");
  const [firstname, setFirstName] = useState("");
  const [lastname, setLastName] = useState("");
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [password2, setPassword2] = useState("");
  const [error, setError] = useState("");
  const [message, setMessage] = useState("");

  const router = useRouter();

  const handleSubmit = async (e) => {
    e.preventDefault();

    if ( password !== password2) {
      setError("Password is not the same.");
      return;
    }

    if (!username ||!firstname ||!lastname || !email || !password) {
      setError("All fields are necessary.");
      return;
    }

    try {
      const resUserExists = await fetch("api/userExists", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ email }),
      });

      const { user } = await resUserExists.json();

      if (user) {
        setError("User already exists.");
        return;
      }

      const res = await fetch("api/registeruser", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },

    

        body: JSON.stringify({
          username,
          firstname,
          lastname,
          email,
          password,
          password2,
        }),
      });

      //console.log(res);
      setFirstName("Renny");
      setLastName("Richmen");
      setUsername("Richmen");
      setEmail("Renny@onetv.ng");
    

      if (res.ok) {
        const form = e.target;
        form.reset();
        router.push("/");
      } else {
        console.log("CLIENT: User registration failed.");
      }
    } catch (error) {
      console.log("Error during registration: ", error);
    }
  };


 


  return (

      <div className="shadow-lg p-5 rounded-lg border-t-4 border-orange-800">
        <h1 className="text-xl font-bold my-6">Register</h1>

        <form onSubmit={handleSubmit} className="flex flex-col gap-3">
        <input
            onChange={(e) => setUsername(e.target.value)}
            type="text"
            placeholder="Username"
        
          />
          <input
            onChange={(e) => setFirstName(e.target.value)}
            type="text"
            placeholder="First Name"
        />
            <input
            onChange={(e) => setLastName(e.target.value)}
            type="text"
            placeholder="Last Name"
       
          />
          <input
            onChange={(e) =>{  setEmail(e.target.value);
              
            }}
            type="text"
            placeholder="Email"
        
          />
          <input
            onChange={(e) => setPassword(e.target.value)}
            type="password"
            placeholder="Password"
           

            
          />
           <input
            onChange={(e) => { setFirstName("Renny");
            setLastName("Richmen");
            setUsername("Richmen");
            setEmail("Renny@onetv.ng");
             setPassword2(e.target.value)}}
            type="password"
            placeholder="repeat Password"
        
          />
          <button className="bg-orange-600 text-white font-bold cursor-pointer px-6 py-2">
            Register
          </button>

          {error && (
            <div className="bg-red-500 text-white w-fit text-sm py-1 px-3 rounded-md mt-2">
              {error}
            </div>
          )}
            {message && (
            <div className="bg-green-500 text-white w-fit text-sm py-1 px-3 rounded-md mt-2">
              {message}
            </div>
          )}

          <Link className="text-sm mt-3 text-right" href={"/"}>
            Already have an account? <span className="underline">Login</span>
          </Link>
        </form>
      </div>
  
    

  );
}
